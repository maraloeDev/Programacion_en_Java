    private void conexionBD() {
        try {
           con = DriverManager.getConnection(URL, USER, PASSWORD);
            JOptionPane.showMessageDialog(null, "Conexion exitosa");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al conectar la base de datos", "Error 0X00001", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
    private void tiposProducto() {
        tiposProducto.setModel(modeloCombo);
        modeloCombo.addElement("(Elige uno)");
        try {
            ps = con.prepareStatement("SELECT * FROM tiposproducto");
            rs = ps.executeQuery();
            
            while(rs.next()) {
                modeloCombo.addElement(rs.getString("tipo"));
            }
            rs.close();
            ps.close();
        } catch (SQLException ex) {
            Logger.getLogger(Ejercicio01_Almacen.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

