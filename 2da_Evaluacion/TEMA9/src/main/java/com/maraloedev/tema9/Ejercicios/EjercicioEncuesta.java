try (CSVReader cSVReader = new CSVReader(new FileReader(archivo))) {
        
        // Obtener fecha y hora actual
        LocalDateTime ahora = LocalDateTime.now();
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss");
        String fechaHora = ahora.format(formato);

        // Construir el mensaje
        StringBuilder mensaje = new StringBuilder();
        mensaje.append("Fecha y hora: ").append(fechaHora).append("\n\n");

        String[] linea;

        while ((linea = cSVReader.readNext()) != null) {
            for (String campo : linea) {
                mensaje.append(campo).append("  "); // Puedes cambiar el separador si quieres
            }
            mensaje.append("\n");
        }

        // Mostrar mensaje en una ventana con scroll
        JTextArea areaTexto = new JTextArea(mensaje.toString());
        areaTexto.setEditable(false);
        areaTexto.setFont(new java.awt.Font("Monospaced", Font.PLAIN, 12));

        JScrollPane scroll = new JScrollPane(areaTexto);
        scroll.setPreferredSize(new java.awt.Dimension(500, 400));

        JOptionPane.showMessageDialog(null, scroll, "Resultados de Encuesta", JOptionPane.INFORMATION_MESSAGE);

    } catch (FileNotFoundException ex) {
        Logger.getLogger(EjercicioEncuesta.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(null, "Archivo no encontrado");
    } catch (IOException | CsvValidationException ex) {
        Logger.getLogger(EjercicioEncuesta.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(null, "Error al leer el archivo");
    }
