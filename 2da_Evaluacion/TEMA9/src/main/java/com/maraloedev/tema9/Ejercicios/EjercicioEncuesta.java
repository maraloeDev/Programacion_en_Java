    private void VotarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_VotarMouseClicked
        try (CSVWriter csvw = new CSVWriter(new FileWriter(archivo))) {
            csvw.writeNext(cabecera);

            Enumeration<AbstractButton> botones = GrupoBotones.getElements();
            AbstractButton boton;

            while (botones.hasMoreElements()) { //Mientras haya mas elementos
                boton = botones.nextElement();
                if (boton.isSelected()) {
                    if (boton.getText().equals("Si")) {
                        votoSi++;
                    } else if (boton.getText().equals("No")) {
                        votoNo++;
                    } else if (boton.getText().equals("NS/NC")) {
                        votoNsNc++;
                    } else {
                        JOptionPane.showMessageDialog(null, "Debe seleccionar una opcion");
                    }
                }
            }

            JOptionPane.showMessageDialog(null, "Voto almacenado correctamente");
            String[] votos = {String.valueOf(votoSi), String.valueOf(votoNo), String.valueOf(votoNsNc)};
            csvw.writeNext(votos);

        } catch (IOException ex) {
            Logger.getLogger(EjercicioEncuesta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_VotarMouseClicked

