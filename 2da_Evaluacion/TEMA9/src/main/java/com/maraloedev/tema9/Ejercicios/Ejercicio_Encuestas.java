    private void conteoVotaciones() {
        Enumeration<AbstractButton> seleccionOpciones = botonesOpciones.getElements();
        AbstractButton boton;

        while (seleccionOpciones.hasMoreElements()) {
            boton = seleccionOpciones.nextElement();

            if (boton.isSelected()) {
                if (boton.getText().equals("Si")) {
                    contadorSi++;
                } else if (boton.getText().equals("No")) {
                    contadorNo++;
                } else if (boton.getText().equals("NS/NC")) {
                    contadorNsNc++;
                }
            }

            btnVerEncuesta.setEnabled(true);
        }

        //Escibir
        try (CSVWriter cSVWriter = new CSVWriter(new FileWriter(f))) {

            String[] encabezado = {"Si,No,NS/NC"};
            String[] puntuaciones = {String.valueOf(contadorSi), String.valueOf(contadorNo), String.valueOf(contadorNsNc)};
            cSVWriter.writeNext(encabezado);
            cSVWriter.writeNext(puntuaciones);

        } catch (IOException ex) {
            Logger.getLogger(Ejercicio_Encuestas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void resultadoEncuesta() {

        JFrame resultadosEncuesta = new JFrame("Resultados");
        JPanel panelVotos = new JPanel();

        panelVotos.setSize(new Dimension(800, 800));
        panelVotos.setLayout(new BorderLayout());

        resultadosEncuesta.add(panelVotos);
        resultadosEncuesta.setSize(new Dimension(500, 500));
        JTextArea votos = new JTextArea();
        votos.setEditable(false);

        //LEER EL CSV
        try (CSVReader csvReader = new CSVReader(new FileReader(f))) {
            String[] linea;
            StringBuilder texto = new StringBuilder();

            while ((linea = csvReader.readNext()) != null) {
                if (linea.length >= 3) {
                    texto.append(
                            "Si= ").append(linea[0]).append(": ").append(
                            "No:").append(linea[1]).append(":").append(
                            "NS/NC ").append(linea[2]);
                }
            }

            votos.setText(texto.toString());

        } catch (FileNotFoundException ex) {
            Logger.getLogger(Ejercicio_Encuestas.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException | CsvValidationException ex) {
            Logger.getLogger(Ejercicio_Encuestas.class.getName()).log(Level.SEVERE, null, ex);
        }

        panelVotos.add(votos);

        resultadosEncuesta.setVisible(true);

        if (resultadosEncuesta.isVisible()) {
            btnVerEncuesta.setEnabled(false);
        }

        resultadosEncuesta.setResizable(false);
        resultadosEncuesta.setLocationRelativeTo(null);
    }

