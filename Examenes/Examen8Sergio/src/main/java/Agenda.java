    private void AnadirContactoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AnadirContactoActionPerformed
        try(Connection conn = conBD()) {
            ps = conn.prepareStatement("INSERT INTO contactos(nombre, telefono, correo) VALUES (?,?,?)");
            
            ps.setString(1, nombreIntroducido.getText());
            ps.setString(2, telefonoIntroducido.getText());
            ps.setString(3, correoElectronico.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Datos insertados");
        } catch (SQLException ex) {
            Logger.getLogger(Agenda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_AnadirContactoActionPerformed

    private void eliminarContactoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarContactoActionPerformed
        try(Connection conn = conBD()) {
            
            ArrayList<Integer> operacion = new ArrayList<>();
            ps = conn.prepareStatement("SELECT id FROM contactos");
            rs = ps.executeQuery();
            
            while(rs.next()) {
                operacion.add(rs.getInt("id"));
                
            }
            
            if(operacion.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No hay IDs en la BD");
            } else {
                
            int id = (int) JOptionPane.showInputDialog(null, "ID a eliminar:", "ELIMINAR CONTACTO", 1, null, operacion.toArray(), operacion.get(0));
            ps = conn.prepareStatement("DELETE FROM contactos WHERE id = ?");
            ps.setInt(1, id);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Contacto eliminado");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Agenda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_eliminarContactoActionPerformed

