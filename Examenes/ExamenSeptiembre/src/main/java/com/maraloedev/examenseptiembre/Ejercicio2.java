    private void botonMostrarPrendasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonMostrarPrendasActionPerformed
        listModel.clear();
        ListaPrendas.setModel(listModel);

        listModel.addElement("DESCRIPCIÃ“N | TALLA | COLOR | PRECIO");

        for (Prenda mostrarPrendas : listaPrendas) {
            listModel.addElement(String.valueOf(mostrarPrendas));
        }
        botonCrearPrendas.setEnabled(false);
    }//GEN-LAST:event_botonMostrarPrendasActionPerformed

    private void botonBuscarPorColorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarPorColorActionPerformed
        String seleccionColor = JOptionPane.showInputDialog(null, "Introduce el nombre a buscar:");
        listModel.clear();
        
        listModel.addElement("Prendas de color " + seleccionColor);
        boolean encontrado = true;
        for (Prenda colorSeleccionado : listaPrendas) {
            if(seleccionColor.equals(colorSeleccionado.getColor())) {
                listModel.addElement(colorSeleccionado.getDescripcion());
                encontrado = false;
            }
            }
            if(encontrado) {
                JOptionPane.showMessageDialog(null, "Color no encontrado", "Informacion", JOptionPane.ERROR_MESSAGE);
               
        }
    }//GEN-LAST:event_botonBuscarPorColorActionPerformed

    private void botonTotalPrendasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonTotalPrendasActionPerformed
        int total =0;
        
        for(Prenda precioTotal : listaPrendas) {
            total+=precioTotal.getPrecio();
        }
        
        JOptionPane.showMessageDialog(null, "El precio total de las prendas es de " + total);
    }//GEN-LAST:event_botonTotalPrendasActionPerformed

